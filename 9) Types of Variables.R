## Types of Variables

#integer
x <- 2L   ## add L after the number to store it as an integer. R by default stores at double level so if you are not going to use it in an arithmetic operation then only add an "L".
typeof(x)
Output :- "integer"

 #double
y <- 2.5
typeof(y)
Output :- "double"


x <- 2
typeof(x)
Output :- "double"



#complex number
z <- 3 +2i
typeof(z)
Output :- "complex"


#charachter
a <- "jordan the rockstar"
typeof(a)
Output :- "character"

#logical
b <- TRUE
typeof(b)
Output :- "logical"


